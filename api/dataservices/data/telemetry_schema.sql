CREATE TABLE IF NOT EXISTS telemetry_events (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    event_id CHAR(32) NOT NULL,
    occurred_at DATETIME(6) NOT NULL,
    received_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
    event_type VARCHAR(64) NOT NULL,
    event_name VARCHAR(160) NOT NULL DEFAULT '',
    page_path VARCHAR(512) NOT NULL DEFAULT '',
    page_query TEXT NULL,
    page_title VARCHAR(255) NOT NULL DEFAULT '',
    referrer_url VARCHAR(2048) NOT NULL DEFAULT '',
    target_url VARCHAR(2048) NOT NULL DEFAULT '',
    action_label VARCHAR(255) NOT NULL DEFAULT '',
    element_tag VARCHAR(32) NOT NULL DEFAULT '',
    element_role VARCHAR(64) NOT NULL DEFAULT '',
    session_id CHAR(36) NOT NULL DEFAULT '',
    visitor_id CHAR(36) NOT NULL DEFAULT '',
    user_email VARCHAR(255) NOT NULL DEFAULT '',
    auth_state VARCHAR(32) NOT NULL DEFAULT 'anonymous',
    selected_country VARCHAR(160) NOT NULL DEFAULT '',
    selected_locale VARCHAR(32) NOT NULL DEFAULT '',
    browser_language VARCHAR(32) NOT NULL DEFAULT '',
    timezone VARCHAR(64) NOT NULL DEFAULT '',
    screen_width INT UNSIGNED NULL,
    screen_height INT UNSIGNED NULL,
    viewport_width INT UNSIGNED NULL,
    viewport_height INT UNSIGNED NULL,
    user_agent VARCHAR(1024) NOT NULL DEFAULT '',
    ip_address VARCHAR(64) NOT NULL DEFAULT '',
    ip_hash CHAR(64) NOT NULL DEFAULT '',
    forwarded_for VARCHAR(512) NOT NULL DEFAULT '',
    country_code VARCHAR(8) NOT NULL DEFAULT '',
    country_name VARCHAR(160) NOT NULL DEFAULT '',
    region_name VARCHAR(160) NOT NULL DEFAULT '',
    city_name VARCHAR(160) NOT NULL DEFAULT '',
    location_source VARCHAR(32) NOT NULL DEFAULT '',
    request_host VARCHAR(255) NOT NULL DEFAULT '',
    request_method VARCHAR(16) NOT NULL DEFAULT '',
    source VARCHAR(32) NOT NULL DEFAULT 'web',
    metadata_json JSON NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uq_telemetry_events_event_id (event_id),
    KEY idx_telemetry_events_occurred_at (occurred_at),
    KEY idx_telemetry_events_type_occurred (event_type, occurred_at),
    KEY idx_telemetry_events_page_occurred (page_path(191), occurred_at),
    KEY idx_telemetry_events_country_occurred (country_code, occurred_at),
    KEY idx_telemetry_events_visitor_occurred (visitor_id, occurred_at),
    KEY idx_telemetry_events_session_occurred (session_id, occurred_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
