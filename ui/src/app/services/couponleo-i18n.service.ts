function interpolate(template: string, params?: CouponleoTranslationParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_, key: string) => {
    const value = params[key];
    return value === null || value === undefined ? '' : String(value);
  });
}

@Injectable({ providedIn: 'root' })
export class CouponleoI18nService {
  private readonly localeService = inject(CouponleoLocaleService);

  readonly locale = this.localeService.locale;
  readonly direction = this.localeService.direction;
  readonly languageTag = this.localeService.languageTag;
  readonly isRtl = computed(() => this.direction() === 'rtl');

  t(key: string, params?: CouponleoTranslationParams): string {
    const locale = this.locale() as CouponleoSupportedLocale;
    const bundle = TRANSLATIONS[locale] ?? TRANSLATIONS[DEFAULT_LOCALE];
    const fallback = TRANSLATIONS[DEFAULT_LOCALE][key] ?? key;
    return interpolate(bundle[key] ?? fallback, params);
  }

  phrase(value: string): string {
    return resolveTranslatedPhrase(this.locale() as CouponleoSupportedLocale, value);
  }

  localize<T>(value: T): T {
    return this.localizeNode(value, '');
  }

  formatNumber(value: number, options?: Intl.NumberFormatOptions): string {
    return new Intl.NumberFormat(this.locale(), options).format(value);
  }

  formatDate(value: Date | number | string, options?: Intl.DateTimeFormatOptions): string {
    const date = value instanceof Date ? value : new Date(value);
    if (Number.isNaN(date.getTime())) {
      return '';
    }

    return new Intl.DateTimeFormat(this.locale(), options).format(date);
  }

  formatRelativeTime(value: Date | number | string): string {
    const date = value instanceof Date ? value : new Date(value);
    if (Number.isNaN(date.getTime())) {
      return '';
    }

    const diffMinutes = Math.round((date.getTime() - Date.now()) / 60_000);
    const formatter = new Intl.RelativeTimeFormat(this.locale(), { numeric: 'auto' });

    if (Math.abs(diffMinutes) < 60) {
      return formatter.format(diffMinutes, 'minute');
    }

    const diffHours = Math.round(diffMinutes / 60);
    if (Math.abs(diffHours) < 24) {
      return formatter.format(diffHours, 'hour');
    }

    const diffDays = Math.round(diffHours / 24);
    if (Math.abs(diffDays) < 14) {
      return formatter.format(diffDays, 'day');
    }

    return this.formatDate(date, { month: 'short', day: 'numeric' });
  }

  localizeSeo(pathname: string, title: string, description: string): { title: string; description: string } {
    const normalizedPath = pathname.replace(/\/+$/, '') || '/';

    if (/^\/stores\/[^/]+$/.test(normalizedPath)) {
      return {
        title: this.phrase(title),
        description: this.phrase(description),
      };
    }

    if (/^\/categories\/[^/]+$/.test(normalizedPath)) {
      return {
        title: this.phrase(title),
        description: this.phrase(description),
      };
    }

    return {
      title: this.phrase(title),
      description: this.phrase(description),
    };
  }

  private localizeNode<T>(value: T, key: string): T {
    if (typeof value === 'string') {
      return (TRANSLATABLE_KEYS.has(key) ? this.phrase(value) : value) as T;
    }

    if (Array.isArray(value)) {
      return value.map((entry) => this.localizeNode(entry, key)) as T;
    }

    if (!value || typeof value !== 'object') {
      return value;
    }

    const nextValue: Record<string, unknown> = {};
    for (const [entryKey, entryValue] of Object.entries(value as Record<string, unknown>)) {
      nextValue[entryKey] = this.localizeNode(entryValue, entryKey);
    }

    return nextValue as T;
  }
}
