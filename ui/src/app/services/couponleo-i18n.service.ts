function resolveTranslatedPhrase(locale: CouponleoSupportedLocale, value: string): string {
  const bundle = EXTRA_PHRASE_TRANSLATIONS[locale];
  if (bundle?.[value]) {
    return bundle[value];
  }

  const direct = translateCouponleoPhrase(locale, value);
  if (direct !== value) {
    return direct;
  }

  const candidates = new Set<string>();
  if (value.length > 1) {
    candidates.add(`${value.charAt(0).toUpperCase()}${value.slice(1)}`);
    candidates.add(`${value.charAt(0).toLowerCase()}${value.slice(1)}`);
  }

  for (const candidate of candidates) {
    const translatedCandidate = bundle?.[candidate] ?? translateCouponleoPhrase(locale, candidate);
    if (translatedCandidate !== candidate) {
      return translatedCandidate;
    }
  }

  return value;
}

