const translationCatalogIndex = translationCatalog as Readonly<Record<string, Readonly<Record<string, string>>>>;

export function normalizeCouponleoLocale(locale: string | null | undefined): CouponleoSupportedLocale {
  const normalizedLocale = String(locale ?? '').trim().toLowerCase();
  const matchedLocale = COUPONLEO_LOCALE_DEFINITIONS.find((definition) => definition.value.toLowerCase() === normalizedLocale);
  return (matchedLocale?.value ?? COUPONLEO_DEFAULT_LOCALE) as CouponleoSupportedLocale;
}

export function couponleoLocaleDefinition(locale: string | null | undefined): CouponleoLocaleDefinition {
  const normalizedLocale = normalizeCouponleoLocale(locale);
  return COUPONLEO_LOCALE_DEFINITIONS.find((definition) => definition.value === normalizedLocale) ?? COUPONLEO_LOCALE_DEFINITIONS[0];
}

export function translateCouponleoPhrase(locale: string | null | undefined, value: string): string {
  const normalizedLocale = normalizeCouponleoLocale(locale);
  if (normalizedLocale === COUPONLEO_DEFAULT_LOCALE) {
    return value;
  }

  return translationCatalogIndex[normalizedLocale]?.[value] ?? value;
}

export function couponleoExpiryLabels(locale: string | null | undefined) {
  const language = normalizeCouponleoLocale(locale).split('-')[0] as keyof typeof expiryLabelCatalog;
  return expiryLabelCatalog[language] ?? expiryLabelCatalog.en;
}
